                INSERT INTO Canbo_Duyet(Mactds, Macb, Thoigianduyet, Ghichu) VALUES(chiTiet.Mactds, p_maqn, SYSDATE, 'T? ch?i ??i ??i');
                UPDATE Chitietdanhsach SET Tinhtrang = 0 WHERE Mactds = chiTiet.Mactds;
            ELSE
                -- danhSachKhongViPham
                IF v_hinhthucrn = 1 THEN
                    IF chiTiet.Diadiem LIKE '%'||v_diachi||'%' THEN
                        INSERT INTO Canbo_Duyet(Mactds, Macb, Thoigianduyet, Ghichu) VALUES(chiTiet.Mactds, p_maqn, SYSDATE, 'T? ch?i ??i ??i');
                        UPDATE Chitietdanhsach SET Tinhtrang = 0 WHERE Mactds = chiTiet.Mactds;
                    ELSE
                        INSERT INTO Canbo_Duyet(Mactds, Macb, Thoigianduyet, Ghichu) VALUES(chiTiet.Mactds, p_maqn, SYSDATE, 'Phê duy?t ??i ??i');
                        UPDATE Chitietdanhsach SET Tinhtrang = 2 WHERE Mactds = chiTiet.Mactds;
                    END IF;
