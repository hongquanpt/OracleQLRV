--------------------------------------------------------
--  DDL for Procedure SP_UPDATE_LICH_SU_JSON
--------------------------------------------------------
set define off;

  CREATE OR REPLACE EDITIONABLE PROCEDURE "C##QUAN"."SP_UPDATE_LICH_SU_JSON" (p_MACTDS IN NUMBER) AS
    v_json CLOB;
BEGIN
    -- Gi? s? truy v?n JOIN tr? v? duy nh?t 1 dòng cho m?i MACTDS
    SELECT JSON_OBJECT(
             'MaCtds' VALUE ct.Mactds,
             'MaHocVien' VALUE ct.Mahocvien,
             'MaGiayTo' VALUE dsgt.Magiayto,
             'DaTra' VALUE gt.Tinhtrang,
             'ThoiGianLay' VALUE dsgt.Thoigianlay,
             'ThoiGianTra' VALUE dsgt.Thoigiantra,
             'SoGiay' VALUE gt.Sogiay,
             'ThoiGianRa' VALUE ct.Thoigianra,
             'ThoiGianVao' VALUE ct.Thoigianvao,
             'TinhTrang' VALUE ct.Tinhtrang,
             'HoTen' VALUE qn.Hoten,
             'DiaChi' VALUE qn.Diachi,
             'MaCv' VALUE qn.Macv,
             'MaDv' VALUE qn.Madv,
             'MaCapBac' VALUE qn.Macapbac,
             'CapBac1' VALUE cb.Capbac,
             'TenCv' VALUE cv.Tencv,
             'TenDv' VALUE dv.Tendv,
             'LyDo' VALUE ct.Lydo,
             'DiaDiem' VALUE ct.Diadiem,
             'HinhThucRn' VALUE ct.Hinhthucrn,
             'NguoiDuyet' VALUE cbd.Macb,
             'ThoiGianRaC' VALUE ra.Thoigianra,
             'ThoiGianVaoC' VALUE ra.Thoigianvao
           )
    INTO v_json
    FROM Chitietdanhsach ct
         JOIN Quannhan qn ON ct.Mahocvien = qn.Maqn
         JOIN Capbac cb ON qn.Macapbac = cb.Macapbac
         JOIN Donvi dv ON qn.Madv = dv.Madv
         JOIN Chucvu cv ON qn.Macv = cv.Macv
         JOIN Chitietdanhsach_Giayto dsgt ON ct.Mactds = dsgt.Mactds
         JOIN Giayto gt ON dsgt.Magiayto = gt.Magiayto
         JOIN Rangoai ra ON ct.Mactds = ra.Mactds
         JOIN Canbo_Duyet cbd ON ct.Mactds = cbd.Mactds
    WHERE cbd.Ghichu = 'Phê duy?t ti?u ?oàn' AND (ct.Tinhtrang = 4 OR ct.Tinhtrang = 0)
      AND ct.Mactds = p_MACTDS;

    -- Upsert vào b?ng LICH_SU_JSON
    MERGE INTO LICH_SU_JSON t
    USING (SELECT p_MACTDS AS MACTDS, v_json AS JSON_DATA FROM DUAL) s
    ON (t.MACTDS = s.MACTDS)
    WHEN MATCHED THEN
        UPDATE SET t.JSON_DATA = s.JSON_DATA
    WHEN NOT MATCHED THEN
        INSERT (MACTDS, JSON_DATA) VALUES (s.MACTDS, s.JSON_DATA);

END;

