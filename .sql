)
AS
 
BEGIN

    FOR chiTiet IN (SELECT * FROM Chitietdanhsach WHERE Tinhtrang = 1) LOOP
        -- Ki?m tra xem Mahocvien có trong Viphams không:
        DECLARE
            v_count NUMBER;
            v_diachi VARCHAR2(2000);
            v_hinhthucrn NUMBER;
        BEGIN
            SELECT COUNT(*) INTO v_count FROM Vipham WHERE Mahv = chiTiet.Mahocvien;

